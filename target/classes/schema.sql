### SNOMED CT DDL

#### Concept
DROP  TABLE IF EXISTS CONCEPT;
CREATE TABLE CONCEPT (
  SEQ bigint(20) NOT NULL AUTO_INCREMENT COMMENT '일련번호',
  ACTIVE tinyint(4) NOT NULL COMMENT '컨셉의 활성여부; 0:비활성, 1:활성',
  EFFECTIVE_TIME char(8) NOT NULL COMMENT '',
  MODULE_ID varchar(18) NOT NULL,
  DEFINITION_STATUS_ID char(18) NOT NULL,
  CONCEPT_ID varchar(18) NOT NULL,
  DATE_CREATED datetime DEFAULT NULL,
  PRIMARY KEY (SEQ),
  KEY IDX_CONCEPT (CONCEPT_ID)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

#### Description
DROP  TABLE IF EXISTS DESCRIPTION;
CREATE TABLE DESCRIPTION_TP (
  SEQ int(11) NOT NULL AUTO_INCREMENT COMMENT '일련번호',
  ACTIVE tinyint(1) NOT NULL COMMENT '활성화 여부',
  EFFECTIVE_TIME char(8) NOT NULL COMMENT '유효일자',
  MODULE_ID varchar(18) NOT NULL,
  TYPE_ID varchar(18) NOT NULL COMMENT '용어의 형태를 나타내는 식별자\n900000000000003001:Fully specified name\n900000000000548007:Preferred\n900000000000013009:Synonym\n900000000000550004:Definition',
  CASE_SIGNIFICANCE_ID varchar(18) NOT NULL COMMENT '대소문자 구분 민감정도를 나타내는 식별자\n900000000000448009:Entire term case insensitive\n900000000000017005:Entire term case sensitive\n900000000000020002:Only initial character case insensitive',
  LANGUAGE_CODE char(2) NOT NULL COMMENT 'ISO639',
  LANGUAGE_CULTURE char(5) DEFAULT NULL COMMENT 'ISO639 + ISO3166',
  TERM varchar(255) NOT NULL COMMENT '용어',
  CONCEPT_ID varchar(18) NOT NULL,
  DESCRIPTION_ID varchar(18) NOT NULL,
  CREATED_DATE datetime DEFAULT NULL,
  PRIMARY KEY (SEQ)
) ENGINE=InnoDB DEFAULT CHARSET=utf8;

